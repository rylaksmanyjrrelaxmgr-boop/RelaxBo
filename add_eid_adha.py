async def add_replies():
    try:
        async with aiosqlite.connect(DB_PATH) as db:
            for keyword, reply in EID_ADHA_REPLIES.items():
                await db.execute(
                    "INSERT OR REPLACE INTO group_replies (keyword, reply) VALUES (?, ?)",
                    (keyword.lower(), reply)
                )
                print(f"✅ تم إضافة: {keyword}")
            await db.commit()
        print("\n🎉 تم إضافة جميع ردود عيد الأضحى بنجاح إلى قاعدة البيانات!")
    except Exception as e:
        print(f"❌ حدث خطأ: {e}")

if __name__ == "__main__":
    asyncio.run(add_replies())
