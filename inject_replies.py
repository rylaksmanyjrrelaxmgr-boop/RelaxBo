# ===================== توليد ردود إضافية =====================
def generate_more_replies(base_list, target=500):
    """توليد ردود إضافية"""
    keywords = [
        "مرحبا", "سلام", "اهلا", "كيفك", "شلونك", "الحمد", "شكرا", "مشكور",
        "تمام", "بخير", "جيد", "ممتاز", "حلو", "زي الفل", "طيب", "حاضر",
        "اوكي", "موافق", "معك", "يسلمو", "حب", "حبيبي", "جميل", "رائع",
    ]
    
    replies = []
    used_keywords = set()
    
    for keyword, reply in base_list:
        if keyword not in used_keywords:
            replies.append((keyword, reply))
            used_keywords.add(keyword)
    
    while len(replies) < target:
        keyword = random.choice(keywords)
        templates = [
            f"{keyword}! كيف يمكنني مساعدتك؟ 😊",
            f"أهلاً بك، كيف يمكنني خدمتك بخصوص {keyword}؟ 🤍",
            f"مرحباً! أنا هنا لمساعدتك في {keyword} 🌸",
            f"شكراً لتواصلك، كيف يمكنني مساعدتك في {keyword}؟ 😊",
            f"أهلاً وسهلاً، أخبرني ماذا تريد عن {keyword} 🤍",
        ]
        reply = random.choice(templates)
        
        if keyword not in used_keywords or random.random() < 0.3:
            replies.append((keyword.lower(), reply))
            used_keywords.add(keyword)
        else:
            new_keyword = keyword + str(random.randint(1, 999))
            replies.append((new_keyword.lower(), reply))
    
    return replies[:target]

# ===================== حقن الردود =====================
def inject_replies():
    """حقن الردود في قاعدة البيانات"""
    try:
        all_replies = generate_more_replies(REPLIES, 500)
        
        conn = sqlite3.connect(DB_PATH)
        cursor = conn.cursor()
        
        cursor.execute("""
            CREATE TABLE IF NOT EXISTS group_replies (
                keyword TEXT PRIMARY KEY,
                reply TEXT
            )
        """)
        
        inserted = 0
        for keyword, reply in all_replies:
            try:
                cursor.execute(
                    "INSERT OR REPLACE INTO group_replies (keyword, reply) VALUES (?, ?)",
                    (keyword.lower(), reply)
                )
                inserted += 1
            except Exception as e:
                print(f"⚠️ فشل إدراج '{keyword}': {e}")
        
        conn.commit()
        conn.close()
        
        print("\n" + "="*60)
        print("✅ تم حقن الردود بنجاح!")
        print(f"📊 إجمالي الردود المحقونة: {inserted}")
        print("="*60)
        
        print("\n📝 عينة من الردود:")
        sample = random.sample(all_replies, min(10, len(all_replies)))
        for keyword, reply in sample:
            print(f"   • {keyword} -> {reply[:50]}...")
        
        return True
        
    except Exception as e:
        print(f"❌ فشل حقن الردود: {e}")
        return False

# ===================== عرض الردود الموجودة =====================
def show_existing_replies():
    """عرض الردود الموجودة"""
    try:
        conn = sqlite3.connect(DB_PATH)
        cursor = conn.cursor()
        cursor.execute("SELECT COUNT(*) FROM group_replies")
        count = cursor.fetchone()[0]
        print(f"\n📊 عدد الردود الموجودة حالياً: {count}")
        conn.close()
        return count
    except Exception as e:
        print(f"⚠️ لا يمكن قراءة قاعدة البيانات: {e}")
        return 0

# ===================== حذف جميع الردود =====================
def clear_all_replies():
    """حذف جميع الردود"""
    try:
        confirm = input("\n⚠️ هل أنت متأكد من حذف جميع الردود؟ (اكتب 'نعم' للتأكيد): ")
        if confirm.lower() != 'نعم':
            print("❌ تم إلغاء العملية")
            return False
        
        conn = sqlite3.connect(DB_PATH)
        cursor = conn.cursor()
        cursor.execute("DELETE FROM group_replies")
        conn.commit()
        conn.close()
        
        print("✅ تم حذف جميع الردود")
        return True
    except Exception as e:
        print(f"❌ فشل حذف الردود: {e}")
        return False

